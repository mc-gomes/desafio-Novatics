def validaTabela(tabela):
    # Analisa se Ã© uma tabela de tamanho 9x9
    
    valoresValidos = ['1', '2', '3', '4', '5', '6', '7', '8', '9', '.', '', ' ']

    if len(tabela) != 9:
        return False
    else:
        for i in range(len(tabela)):
            if len(tabela[i]) != 9:
                return False

    for i in range(len(tabela)):
        for j in range(len(tabela[i])):
            if tabela[i][j] not in valoresValidos:
                return False

    return True


